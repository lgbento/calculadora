
public class Operacoes {
	
	private double x;
	private double y;
	
	public double somar(double x, double y) {
		return x+y;
	}
	
	public double subtrair(double x, double y) {
		return x-y;
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}
	
	
	
}
